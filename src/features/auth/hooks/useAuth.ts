import { useState } from 'react';
import { useNavigate } from 'react-router-dom';
import { authService, type AuthCredentials } from '../services/authService';
import { useAuthStore } from '../../../app/store/useAuthStore';

export const useAuth = () => {
  const [isLoading, setIsLoading] = useState(false);
  const { user, setUser } = useAuthStore();
  const navigate = useNavigate();

  const signUp = async ({ email, password, name }: AuthCredentials) => {
    setIsLoading(true);
    try {
      const { user, error } = await authService.signUp({ email, password, name });

      if (error) throw error;

      if (user) {
        setUser(user);
      }

      return { success: true };
    } catch (error) {
      return { success: false, error: error as Error };
    } finally {
      setIsLoading(false);
    }
  };

  const signIn = async ({ email, password }: AuthCredentials) => {
    setIsLoading(true);
    try {
      const { user, error } = await authService.signIn({ email, password });

      if (error) throw error;

      if (user) {
        setUser(user);
        navigate('/dashboard');
      }

      return { success: true };
    } catch (error) {
      return { success: false, error: error as Error };
    } finally {
      setIsLoading(false);
    }
  };

  const signInWithGoogle = async () => {
    console.log('游리 useAuth.signInWithGoogle called');
    setIsLoading(true);
    try {
      console.log('游리 Calling authService.signInWithGoogle...');
      const result = await authService.signInWithGoogle();

      console.log('游리 authService result:', result);

      if (result.error) {
        console.error('游댮 useAuth error:', result.error);
        throw result.error;
      }

      // Google OAuth will redirect, so we don't handle user state here
      console.log('游릭 useAuth returning success');
      return { success: true };
    } catch (error) {
      console.error('游댮 useAuth catch error:', error);
      return { success: false, error: error as Error };
    } finally {
      setIsLoading(false);
      console.log('游리 useAuth setIsLoading(false)');
    }
  };

  const signOut = async () => {
    setIsLoading(true);
    try {
      const { error } = await authService.signOut();

      if (error) throw error;

      setUser(null);
      navigate('/auth');

      return { success: true };
    } catch (error) {
      return { success: false, error: error as Error };
    } finally {
      setIsLoading(false);
    }
  };

  return {
    user,
    isLoading,
    signUp,
    signIn,
    signInWithGoogle,
    signOut,
    isAuthenticated: !!user,
  };
};